package com.bank.app.service.Office;

public class ReviewData {
	
	private String name;
	private String pid;
	private String phone;
	private String email;
	private String dob;
	private String gender;
	private String county;
	private String box;
	private String employment;
	private String salary;
	private String bankacc;
	private String bankname;
	private String imagename;
	
	
	public ReviewData(String name, String pid, String phone, String email, String dob, String gender, String county,
			String box, String employment, String salary, String bankacc, String bankname, String imagename) {
		super();
		this.name = name;
		this.pid = pid;
		this.phone = phone;
		this.email = email;
		this.dob = dob;
		this.gender = gender;
		this.county = county;
		this.box = box;
		this.employment = employment;
		this.salary = salary;
		this.bankacc = bankacc;
		this.bankname = bankname;
		this.imagename = imagename;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDob() {
		return dob;
	}
	public void setDob(String dob) {
		this.dob = dob;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getCounty() {
		return county;
	}
	public void setCounty(String county) {
		this.county = county;
	}
	public String getBox() {
		return box;
	}
	public void setBox(String box) {
		this.box = box;
	}
	public String getEmployment() {
		return employment;
	}
	public void setEmployment(String employment) {
		this.employment = employment;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}
	public String getBankacc() {
		return bankacc;
	}
	public void setBankacc(String bankacc) {
		this.bankacc = bankacc;
	}
	public String getBankname() {
		return bankname;
	}
	public void setBankname(String bankname) {
		this.bankname = bankname;
	}
	public String getImagename() {
		return imagename;
	}
	public void setImagename(String imagename) {
		this.imagename = imagename;
	}
	
	
	
	
	

}
